import java.util.List;

public class MyObject {
	private String name;
	private List<Integer> colorNumber;
	private int gSize;
	private List<Integer> x_list;
	private List<Integer> y_list;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public List<Integer> getColorNumber() {
		return colorNumber;
	}
	public void setColorNumber(List<Integer> colorNumber) {
		this.colorNumber = colorNumber;
	}
	public int getgSize() {
		return gSize;
	}
	public void setgSize(int gSize) {
		this.gSize = gSize;
	}
	public List<Integer> getX_list() {
		return x_list;
	}
	public void setX_list(List<Integer> x_list) {
		this.x_list = x_list;
	}
	public List<Integer> getY_list() {
		return y_list;
	}
	public void setY_list(List<Integer> y_list) {
		this.y_list = y_list;
	}


}
